    /**
     * To get all the rooms where guests are currently staying
     */
    public void refreshRoomNoComboBox() {

        GuestRegisterData checkOutData = new GuestRegisterData();

        DefaultComboBoxModel roomNoComboBoxModel = (DefaultComboBoxModel) roomNoComboBox.getModel();

        ArrayList<GuestRegister> guestRegisterList;
        Iterator<GuestRegister> guestRegisterListIterator;

        roomNoComboBoxModel.removeAllElements();

        guestRegisterList = checkOutData.getOccupiedRooms();
        guestRegisterListIterator = guestRegisterList.iterator();

        while (guestRegisterListIterator.hasNext()) {

            GuestRegister guestRegister = guestRegisterListIterator.next();

            roomNoComboBoxModel.addElement(guestRegister.getRoomNo());

        }

    }

    /**
     * To calculate the total amount payable
     */
    public void payableBill() {

        Billing billing = new Billing();
        int roomNo, bookingID = 0;
        Date checkInDate, checkOutDate;

        try {

            roomNo = Integer.parseInt(roomNoComboBox.getSelectedItem().toString());
            checkInDate = new java.sql.Date(new SimpleDateFormat("yyyy-mm-dd").parse(checkInDateTextField.getText()).getTime());
            checkOutDate = new java.sql.Date(checkOutDateChooser.getDate().getTime());

            if (!(bookingIDTextField.getText().equals(""))) {
                if (!(bookingIDTextField.getText().substring(9).equals(""))) {
                    bookingID = Integer.parseInt(bookingIDTextField.getText().substring(9));
                }
            }

            totalAmountTextField.setText(billing.getTotalAmount(roomNo, checkInDate, checkOutDate) + "");
            amountPayableTextField.setText(billing.getPayableAmount(roomNo, bookingID, checkInDate, checkOutDate) + "");

        } catch (ParseException ex) {
            Logger.getLogger(CheckOutForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


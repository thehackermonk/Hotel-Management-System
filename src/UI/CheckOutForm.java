    private void checkOutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutButtonActionPerformed

        GuestRegisterData guestRegisterData = new GuestRegisterData();

        int roomNo;
        java.sql.Date checkInDate, checkOutDate;

        try {

            roomNo = Integer.parseInt(roomNoComboBox.getSelectedItem().toString());
            checkInDate = new java.sql.Date(new SimpleDateFormat("yyyy-mm-dd").parse(checkInDateTextField.getText()).getTime());
            checkOutDate = new java.sql.Date(checkOutDateChooser.getDate().getTime());

            if (guestRegisterData.checkOut(roomNo, checkInDate, checkOutDate)) {
                JOptionPane.showMessageDialog(null, "Check out complete.");
            }

        } catch (HeadlessException | NumberFormatException | ParseException e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_checkOutButtonActionPerformed


    /**
     * Refresh the values of room table
     *
     * @param table
     */
    public void refreshFoodTable(DefaultTableModel table) {

        FoodData foodData = new FoodData();

        ArrayList<Food> foodList = foodData.getAllFoodDetails();
        Iterator<Food> foodListIterator = foodList.iterator();

        while (table.getRowCount() > 0) {
            table.removeRow(0);
        }

        while (foodListIterator.hasNext()) {

            Food food = foodListIterator.next();
            table.addRow(new Object[]{food.getName(), food.getDescription(), food.getPrice()});

        }

    }

    /**
     * Refresh the values of food name combo box
     *
     * @param comboBox
     */
    public void refreshFoodNameCombo(DefaultComboBoxModel comboBox) {

        FoodData foodData = new FoodData();

        ArrayList<Food> foodList = foodData.getAllFoodDetails();
        Iterator<Food> foodListIterator = foodList.iterator();

        comboBox.removeAllElements();

        while (foodListIterator.hasNext()) {

            Food food = foodListIterator.next();
            comboBox.addElement(food.getName());

        }

    }


    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        FoodData foodData = new FoodData();
        Food food = new Food();

        if ("".equals(addNameTextField.getText())) {

            JOptionPane.showMessageDialog(null, "Please enter name");

        } else if ("".equals(addPriceTextField.getText())) {

            JOptionPane.showMessageDialog(null, "Please enter price");

        } else {
            food.setNdbNo(Integer.parseInt(addNDBNoTextField.getText()));
            food.setName(addNameTextField.getText());
            food.setDescription(addDescriptionTextArea.getText());
            food.setPrice(Double.parseDouble(addPriceTextField.getText()));

            if (foodData.addNewFood(food)) {

                JOptionPane.showMessageDialog(null, food.getName() + " added successfully!");
                addFoodDialog.setVisible(false);

            }

        }

    }//GEN-LAST:event_addButtonActionPerformed


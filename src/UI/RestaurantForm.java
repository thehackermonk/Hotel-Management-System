    private void removeRestaurantButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeRestaurantButtonActionPerformed

        RestaurantData restaurantData = new RestaurantData();

        String name = removeRestaurantNameComboBox.getSelectedItem().toString();

        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure to delete " + name);

        if (confirm == 0) {

            if (restaurantData.removeRestaurant(name)) {

                JOptionPane.showMessageDialog(null, name + " removed successfully!");
                removeRestaurantDialog.setVisible(false);

            } else {

                JOptionPane.showMessageDialog(null, name + " unable to remove.");
                removeRestaurantNameComboBox.requestFocus();

            }

        }

    }//GEN-LAST:event_removeRestaurantButtonActionPerformed


    private void addRestaurantButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRestaurantButtonActionPerformed

        Restaurant restaurant = new Restaurant();
        RestaurantData restaurantData = new RestaurantData();

        restaurant.setName(addNameTextField.getText());
        restaurant.setType(addTypeComboBox.getSelectedItem().toString());

        if (restaurantData.addNewRestaurant(restaurant)) {

            JOptionPane.showMessageDialog(null, "Restaurant " + restaurant.getName() + " added successfully!");
            addRestaurantDialog.setVisible(false);

        } else {

            JOptionPane.showMessageDialog(null, "Addition of restaurant failed!");

        }

    }//GEN-LAST:event_addRestaurantButtonActionPerformed

    private void modifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyButtonActionPerformed

        Sort sort = new Sort();

        DefaultComboBoxModel nameComboBoxModel = (DefaultComboBoxModel) modifyRestaurantNameComboBox.getModel();
        DefaultComboBoxModel typeComboBoxModel = (DefaultComboBoxModel) modifyRestaurantTypeComboBox.getModel();

        modifyRestaurantDialog.setSize(800, 500);
        modifyRestaurantDialog.show();

        refreshRestaurantNameComboBox(nameComboBoxModel);

        sort.sortComboBox(nameComboBoxModel);

        modifyRestaurantNameTextField.setText("");
        refreshRestaurantTypeComboBox(typeComboBoxModel);

    }//GEN-LAST:event_modifyButtonActionPerformed

    private void modifyRestaurantNameComboBoxFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_modifyRestaurantNameComboBoxFocusLost

        Restaurant restaurant;
        RestaurantData restaurantData = new RestaurantData();

        String restaurantName = modifyRestaurantNameComboBox.getSelectedItem().toString();

        restaurant = restaurantData.getRestaurantDetails(restaurantName);

        modifyRestaurantNameTextField.setText(restaurant.getName());
        modifyRestaurantTypeComboBox.setSelectedItem(restaurant.getType());

    }//GEN-LAST:event_modifyRestaurantNameComboBoxFocusLost

    private void modifyRestaurantButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyRestaurantButtonActionPerformed

        Restaurant restaurant = new Restaurant();
        RestaurantData restaurantData = new RestaurantData();

        String name = modifyRestaurantNameComboBox.getSelectedItem().toString();
        restaurant.setName(modifyRestaurantNameTextField.getText());
        restaurant.setType(modifyRestaurantTypeComboBox.getSelectedItem().toString());

        if (restaurantData.modifyRestaurant(restaurant, name)) {

            JOptionPane.showMessageDialog(null, name + " modified successfully!");
            modifyRestaurantDialog.setVisible(false);

        } else {

            modifyRestaurantNameTextField.setText("");
            modifyRestaurantNameComboBox.requestFocus();

        }

    }//GEN-LAST:event_modifyRestaurantButtonActionPerformed


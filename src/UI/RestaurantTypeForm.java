    /**
     * To refresh contents of the restaurant type table
     */
    public void refreshRestaurantTypeTable() {

        RestaurantTypeData restaurantTypeData = new RestaurantTypeData();

        DefaultTableModel restaurantTypeTableModel = (DefaultTableModel) restaurantTypeTable.getModel();

        ArrayList<RestaurantType> restaurantTypeList = restaurantTypeData.getAllRestauarantTypes();
        Iterator<RestaurantType> restaurantTypeListIterator = restaurantTypeList.iterator();

        while (restaurantTypeTableModel.getRowCount() > 0) {
            restaurantTypeTableModel.removeRow(0);
        }

        while (restaurantTypeListIterator.hasNext()) {

            RestaurantType restaurantType = restaurantTypeListIterator.next();

            restaurantTypeTableModel.addRow(new Object[]{restaurantType.getType(), restaurantType.getDescription()});

        }

    }

    /**
     * To refresh contents of the restaurant type combo box
     * 
     * @param comboBox 
     */
    public void refreshRestaurantTypeComboBox(DefaultComboBoxModel comboBox) {

        RestaurantTypeData restaurantTypeData = new RestaurantTypeData();

        ArrayList<RestaurantType> restaurantTypeList = restaurantTypeData.getAllRestauarantTypes();
        Iterator<RestaurantType> restaurantTypeListIterator = restaurantTypeList.iterator();

        comboBox.removeAllElements();

        while (restaurantTypeListIterator.hasNext()) {

            RestaurantType restaurantType = restaurantTypeListIterator.next();
            comboBox.addElement(restaurantType.getType());

        }

    }

    /**
     * To check if the restaurant type is available
     * Restaurant type should be unique
     * 
     * @param type: type of restaurant
     * @return 
     */
    public boolean restaurantTypeIsAvailable(String type) {

        RestaurantTypeData restaurantTypeData = new RestaurantTypeData();

        boolean isAvailable = true;

        ArrayList<RestaurantType> restaurantTypeList = restaurantTypeData.getAllRestauarantTypes();
        Iterator<RestaurantType> restaurantTypeListIterator = restaurantTypeList.iterator();

        while (restaurantTypeListIterator.hasNext()) {

            RestaurantType restaurantType = restaurantTypeListIterator.next();

            if (restaurantType.getType().equalsIgnoreCase(type)) {

                isAvailable = false;
                break;

            }

        }

        return isAvailable;

    }


    private void addRestaurantTypeTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_addRestaurantTypeTextFieldKeyReleased

        String restaurantType = addRestaurantTypeTextField.getText();

        if (restaurantTypeIsAvailable(restaurantType)) {

            addRestaurantTypeTextField.setToolTipText("");
            addButton.setEnabled(true);

        } else {

            addRestaurantTypeTextField.setToolTipText("Restaurant Type already exist!");
            addButton.setEnabled(false);

        }

    }//GEN-LAST:event_addRestaurantTypeTextFieldKeyReleased

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        RestaurantType restaurantType = new RestaurantType();
        RestaurantTypeData restaurantTypeData = new RestaurantTypeData();

        restaurantType.setType(addRestaurantTypeTextField.getText());
        restaurantType.setDescription(addDescriptionTextArea.getText());

        if (restaurantTypeData.addRestaurantType(restaurantType)) {

            JOptionPane.showMessageDialog(null, "Restaurant type added successfully!");
            addTypeDialog.setVisible(false);

        } else {

            JOptionPane.showMessageDialog(null, "Addition of Restaurant type failed");
            clearAddDialog();
            addRestaurantTypeTextField.requestFocus();

        }

    }//GEN-LAST:event_addButtonActionPerformed

